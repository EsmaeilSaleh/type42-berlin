/*
**  type42-berlin — Typing Trainer for 42 Network
**  Function: ft_striteri — Practice Mode: Copy / Recall
*/

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
